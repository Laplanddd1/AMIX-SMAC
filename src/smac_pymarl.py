        self.attn_heads = getattr(args, "attn_heads", 4)
        self.use_residual = getattr(args, "use_residual", True)
        self.use_layer_norm = getattr(args, "use_layer_norm", True)

        # 改进的状态嵌入：深层 MLP 更有利于 Attention 提取特征
        self.state_embed = nn.Sequential(
            nn.Linear(self.state_dim, self.embed_dim),
            nn.ReLU(),
            nn.Linear(self.embed_dim, self.embed_dim * self.n_agents)
        )

        # 多头自注意力层
        self.attn = nn.MultiheadAttention(embed_dim=self.embed_dim, num_heads=self.attn_heads, batch_first=True)

        # 规范化层
        if self.use_layer_norm:
            self.norm1 = nn.LayerNorm(self.embed_dim)
            self.norm2 = nn.LayerNorm(self.embed_dim)

        # 前馈网络(用于增强特征非线性)
        self.ffn = nn.Sequential(
            nn.Linear(self.embed_dim, self.embed_dim * 2),
            nn.ReLU(),
            nn.Linear(self.embed_dim * 2, self.embed_dim)
        )

